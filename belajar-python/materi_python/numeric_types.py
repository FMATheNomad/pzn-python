angka_bulat = 5
angka_pecahan = 2.5

hasil_penjumlahan = angka_bulat + angka_pecahan
print(hasil_penjumlahan)  # Output: 7.5 (tipe float)

hasil_pengurangan = angka_bulat - angka_pecahan
print(hasil_pengurangan)  # Output: 2.5 (tipe float)

hasil_perkalian = angka_bulat * angka_pecahan
print(hasil_perkalian)  # Output: 12.5 (tipe float)

hasil_pembagian = angka_bulat / angka_pecahan
print(hasil_pembagian)  # Output: 2.0 (tipe float)

hasil_pangkat = angka_bulat ** angka_pecahan
print(hasil_pangkat)  # Output: 55.90169943749474 (tipe float)
